# ---------------------------------------------------------------------------
# Private helpers
# ---------------------------------------------------------------------------

def _iou_badge(iou: float) -> str:
    if iou >= 0.5:
        cls = "badge g"
    elif iou >= 0.3:
        cls = "badge y"
    else:
        cls = "badge r"
    return f'<span class="{cls}">{iou:.3f}</span>'


def _build_summary_table(results: List[Dict[str, Any]]) -> str:
    rows: List[str] = []
    for r in results:
        s = r.get("summary", {})
        tracker = s.get("tracker", s.get("tracker_name", "—"))
        dataset = s.get("dataset", s.get("dataset_name", "—"))
        mean_iou = float(s.get("mean_iou", 0.0))
        fps = float(s.get("mean_fps", 0.0))
        mem = float(s.get("peak_memory_mb", 0.0))
        n_seq = s.get("num_sequences", "—")
        energy = s.get("total_energy_j")
        energy_cell = f"{energy:.4f}" if energy is not None else "—"
        rows.append(
            f"    <tr>"
            f"<td><strong>{tracker}</strong></td>"
            f"<td>{dataset}</td>"
            f"<td class='r'>{_iou_badge(mean_iou)}</td>"
            f"<td class='r'>{fps:.1f}</td>"
            f"<td class='r'>{mem:.1f}</td>"
            f"<td class='r'>{energy_cell}</td>"
            f"<td class='r'>{n_seq}</td>"
            f"</tr>"
        )
    header = (
        "  <table>\n"
        "    <thead><tr>"
        "<th>Tracker</th>"
        "<th>Dataset</th>"
        "<th class='r'>mIoU</th>"
        "<th class='r'>FPS</th>"
        "<th class='r'>Mem (MB)</th>"
        "<th class='r'>Energy (J)</th>"
        "<th class='r'>Sequences</th>"
        "</tr></thead>\n"
        "    <tbody>\n"
    )
    return header + "\n".join(rows) + "\n    </tbody>\n  </table>\n"


def _build_sequence_section(result: Dict[str, Any]) -> str:
    s = result.get("summary", {})
    tracker = s.get("tracker", s.get("tracker_name", "—"))
    dataset = s.get("dataset", s.get("dataset_name", "—"))
    sequences = result.get("sequences", [])
    if not sequences:
        return ""

    rows: List[str] = []
    for seq in sequences:
        name = seq.get("sequence_name", "—")
        miou = float(seq.get("mean_iou", 0.0))
        fps = float(seq.get("fps", 0.0))
        lat = float(seq.get("mean_latency_ms", 0.0))
        mem = float(seq.get("peak_memory_mb", 0.0))
        rows.append(
            f"      <tr>"
            f"<td>{name}</td>"
            f"<td class='r'>{_iou_badge(miou)}</td>"
            f"<td class='r'>{fps:.1f}</td>"
            f"<td class='r'>{lat:.2f}</td>"
            f"<td class='r'>{mem:.1f}</td>"
            f"</tr>"
        )

    table = (
        "    <table>\n"
        "      <thead><tr>"
        "<th>Sequence</th>"
        "<th class='r'>mIoU</th>"
        "<th class='r'>FPS</th>"
        "<th class='r'>Latency (ms)</th>"
        "<th class='r'>Mem (MB)</th>"
        "</tr></thead>\n"
        "      <tbody>\n"
        + "\n".join(rows)
        + "\n      </tbody>\n    </table>"
    )

    return (
        f"\n  <details>\n"
        f"    <summary>"
        f"<h3>{tracker} &mdash; {dataset}"
        f"&nbsp;<small style='font-weight:400;color:#666'>({len(sequences)} sequences)</small>"
        f"</h3></summary>\n"
        + table
        + "\n  </details>"
    )


def _try_embed_plots(results: List[Dict[str, Any]]) -> str:
    """Generate and base64-embed FPS + accuracy bar charts. Returns '' if matplotlib is absent."""
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except ImportError:
        return ""

    labels = [
        r.get("summary", {}).get("tracker", r.get("summary", {}).get("tracker_name", "?"))
        for r in results
    ]
    fps_vals = [float(r.get("summary", {}).get("mean_fps", 0.0)) for r in results]
    iou_vals = [float(r.get("summary", {}).get("mean_iou", 0.0)) for r in results]

    cards: List[str] = []

    def _bar_chart(values: List[float], xlabel: str, title: str, xlim: Optional[float]) -> str:
        colors = []
        for v in values:
            if xlabel == "Frames per Second":
                colors.append("#4a6ee0" if v >= 25 else "#f0a500" if v >= 10 else "#e04a4a")
            else:
                colors.append("#4a6ee0" if v >= 0.5 else "#f0a500" if v >= 0.3 else "#e04a4a")

        height = max(2.5, len(labels) * 0.65)
        fig, ax = plt.subplots(figsize=(6, height))
        ax.barh(labels, values, color=colors, edgecolor="white", height=0.55)
        ax.set_xlabel(xlabel, fontsize=9)
        ax.set_title(title, fontsize=10, fontweight="bold", pad=8)
        if xlim is not None:
            ax.set_xlim(0, xlim)
        ax.spines["top"].set_visible(False)
        ax.spines["right"].set_visible(False)
        ax.tick_params(labelsize=8)
        plt.tight_layout()
        buf = io.BytesIO()
        fig.savefig(buf, format="png", dpi=96, bbox_inches="tight")
        plt.close(fig)
        return base64.b64encode(buf.getvalue()).decode()

    if labels:
        b64 = _bar_chart(fps_vals, "Frames per Second", "Throughput Comparison", None)
        cards.append(
            f"    <div class='plot-card'>\n"
            f"      <img src='data:image/png;base64,{b64}' alt='FPS Comparison'>\n"
            f"    </div>"
        )
        b64 = _bar_chart(iou_vals, "Mean IoU", "Accuracy Comparison", 1.0)
        cards.append(
            f"    <div class='plot-card'>\n"
            f"      <img src='data:image/png;base64,{b64}' alt='Accuracy Comparison'>\n"
            f"    </div>"
        )

    if not cards:
        return ""

    return (
        "\n  <h2>Visual Summary</h2>\n"
        "  <div class='plots-grid'>\n"
        + "\n".join(cards)
        + "\n  </div>\n"
    )


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

class HTMLReporter:
    """Generate self-contained HTML benchmark reports from EOVOT results.

    The output file has zero external dependencies: all CSS, JavaScript (none
    needed), and chart images are embedded inline.  It can be opened directly
    in any browser and shared as a single file.

    Args:
        output_dir: Directory where HTML files are written.
            Created automatically if it does not exist.

    Example::

        from eovot.benchmark.engine import BenchmarkEngine
        from eovot.reporting.html_reporter import HTMLReporter

        engine = BenchmarkEngine()
        result = engine.run(tracker, dataset, dataset_name="OTB100")

        reporter = HTMLReporter(output_dir="results/")
        reporter.save(
            results=[result.to_dict()],
            name="mosse-otb100",
            title="MOSSE on OTB-100",
        )
    """

    def __init__(self, output_dir: str = "results/") -> None:
        self.output_dir = Path(output_dir)
        self.output_dir.mkdir(parents=True, exist_ok=True)

    def save(
        self,
        results: List[Dict[str, Any]],
        name: str,
        title: str = "EOVOT Benchmark Report",
        embed_plots: bool = True,
    ) -> Path:
        """Render and save a self-contained HTML report.

        Args:
            results: List of ``result.to_dict()`` dicts produced by
                :class:`~eovot.benchmark.engine.BenchmarkEngine`.
                Pass a single-element list for a single-tracker report.
            name: Output filename base (without ``.html`` extension).
            title: Page heading displayed in the browser tab and report header.
            embed_plots: If ``True`` and matplotlib is available, embed
                base64-encoded comparison charts. Disable when matplotlib is
                not installed or for smaller file sizes.

        Returns:
            :class:`pathlib.Path` of the written HTML file.
        """
        summary_table = _build_summary_table(results)
        sequence_sections = "".join(_build_sequence_section(r) for r in results)
        plots_section = _try_embed_plots(results) if embed_plots else ""

        html = _HTML_TEMPLATE.format(
            title=title,
            version=_EOVOT_VERSION,
            timestamp=datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC"),
            css=_CSS,
            summary_table=summary_table,
            sequence_sections=("\n  <h2>Per-Sequence Breakdown</h2>" + sequence_sections)
            if sequence_sections
            else "",
            plots_section=plots_section,
        )

        path = self.output_dir / f"{name}.html"
        with open(path, "w", encoding="utf-8") as fh:
            fh.write(html)
        return path
