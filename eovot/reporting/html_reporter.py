"""Self-contained HTML report generator for EOVOT benchmarks.

Produces a single ``.html`` file with no external dependencies that includes:

* A tracker comparison summary table.
* Per-sequence metric breakdowns as expandable ``<details>`` sections.
* Embedded base64-encoded FPS and accuracy comparison charts when
  matplotlib is installed (gracefully omitted otherwise).

The report is designed for sharing with collaborators who lack a Python
environment, embedding in project pages, or archiving alongside JSON/CSV
exports as a human-readable result snapshot.

Usage::

    from eovot.reporting.html_reporter import HTMLReporter

    reporter = HTMLReporter(output_dir="results/")
    reporter.save(
        results=[mosse_dict, kcf_dict],
        name="classical-trackers-otb100",
        title="Classical Trackers on OTB-100",
    )
"""

